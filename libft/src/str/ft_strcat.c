#include <string.h>

char	*ft_strcat(char *s1, const char *s2)
{
	size_t		len;
	size_t		i;

	len = 0;
	while (s1[len] != '\0')
		len++;
	i = 0;
	while (s2[i] != '\0')
	{
		s1[len + i] = s2[i];
		i++;
	}
	s1[len + i] = '\0';
	return (s1);
}
