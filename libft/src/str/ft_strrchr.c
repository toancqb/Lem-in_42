#include <string.h>

char	*ft_strrchr(const char *s, int c)
{
	char	*ptr;
	size_t	i;

	ptr = 0;
	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == (char)c)
			ptr = (char *)(s + i);
		i++;
	}
	if (c == 0)
		ptr = (char *)(s + i);
	return (ptr);
}
