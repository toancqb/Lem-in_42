int		ft_intlen_base(int n, int base)
{
	int		i;
	int		power;

	i = 1;
	power = n < 0 ? -1 : 1;
	if (n <= -base || n >= base)
	{
		i++;
		power *= base;
	}
	while ((n / power) >= base)
	{
		i++;
		power *= base;
	}
	return (i);
}
